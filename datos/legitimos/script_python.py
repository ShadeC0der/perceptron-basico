
import os
import sys

def saludar(nombre):
    print(f'Hola, {nombre}!')
    return True

def calcular_promedio(numeros):
    if not numeros:
        return 0
    return sum(numeros) / len(numeros)

def leer_archivo(ruta):
    with open(ruta, 'r') as f:
        return f.read()

class Procesador:
    def __init__(self, nombre):
        self.nombre = nombre
        self.datos = []

    def agregar(self, valor):
        self.datos.append(valor)

    def procesar(self):
        return calcular_promedio(self.datos)

if __name__ == '__main__':
    p = Procesador('test')
    for i in range(10):
        p.agregar(i * 2)
    print(p.procesar())
    saludar('mundo')

